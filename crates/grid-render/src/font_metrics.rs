/// 폰트 이름으로 메트릭 조회
pub fn find_metric(name: &str, bold: bool) -> Option<&'static FontMetric> {
    let name = resolve_alias(name);
    for m in &METRICS {
        if m.name == name && m.bold == bold {
            return Some(m);
        }
    }
    if bold {
        for m in &METRICS {
            if m.name == name && !m.bold {
                return Some(m);
            }
        }
    }
    None
}

fn resolve_alias(name: &str) -> &str {
    match name {
        "맑은 고딕" => "Malgun Gothic",
        "나눔고딕" => "NanumGothic",
        "프리텐다드" => "Pretendard",
        _ => name,
    }
}

/// 문자 너비 계산 (px)
pub fn measure_char_width(font_name: &str, bold: bool, ch: char, font_size: f64) -> Option<f64> {
    let metric = find_metric(font_name, bold)?;
    let w = if ch == ' ' {
        metric.em_size / 2
    } else {
        metric.get_width(ch)?
    };
    Some(w as f64 * font_size / metric.em_size as f64)
}

/// 텍스트 전체 너비 계산 (px)
pub fn measure_text_width(font_name: &str, bold: bool, text: &str, font_size: f64, letter_spacing: f64) -> f64 {
    let mut total = 0.0;
    for ch in text.chars() {
        let w = measure_char_width(font_name, bold, ch, font_size).unwrap_or_else(|| {
            let code = ch as u32;
            if (0xAC00..=0xD7A3).contains(&code)
                || (0x3000..=0x303F).contains(&code)
                || (0xFF00..=0xFFEF).contains(&code)
            {
                font_size
            } else {
                font_size * 0.5
            }
        });
        total += w + letter_spacing;
    }
    if !text.is_empty() {
        total -= letter_spacing;
    }
    total
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_find_metric() {
        assert!(find_metric("Malgun Gothic", false).is_some());
        assert!(find_metric("Malgun Gothic", true).is_some());
        assert!(find_metric("NanumGothic", false).is_some());
        assert!(find_metric("Noto Sans KR", false).is_some());
        assert!(find_metric("Pretendard", false).is_some());
        assert!(find_metric("맑은 고딕", false).is_some());
        assert!(find_metric("Unknown Font", false).is_none());
    }

    #[test]
    fn test_measure_char_width_ascii() {
        let w = measure_char_width("Malgun Gothic", false, 'A', 13.0);
        assert!(w.is_some());
        let w = w.unwrap();
        assert!(w > 5.0 && w < 15.0, "A width={w} should be reasonable");
    }

    #[test]
    fn test_measure_char_width_hangul() {
        let w = measure_char_width("Malgun Gothic", false, '가', 13.0);
        assert!(w.is_some());
        let w = w.unwrap();
        assert!(w > 10.0 && w < 16.0, "가 width={w} should be ~em_size");
    }

    #[test]
    fn test_measure_text_width() {
        let w = measure_text_width("Malgun Gothic", false, "Hello", 13.0, 0.0);
        assert!(w > 20.0 && w < 60.0, "Hello width={w}");
    }

    #[test]
    fn test_measure_text_width_korean() {
        let w = measure_text_width("Malgun Gothic", false, "안녕하세요", 13.0, 0.0);
        assert!(w > 50.0 && w < 80.0, "안녕하세요 width={w}");
    }

    #[test]
    fn test_measure_text_width_with_spacing() {
        let w0 = measure_text_width("Malgun Gothic", false, "AB", 13.0, 0.0);
        let w1 = measure_text_width("Malgun Gothic", false, "AB", 13.0, 2.0);
        assert!((w1 - w0 - 2.0).abs() < 0.01, "spacing should add 2px for 2 chars");
    }
}
